package pro0421.tea;

public class Tea {

	private String teaName;
	private Double teaPrice;
	private String teaFlavor;
	public String getTeaName() {
		return teaName;
	}
	public void setTeaName(String teaName) {
		this.teaName = teaName;
	}
	public Double getTeaPrice() {
		return teaPrice;
	}
	public void setTeaPrice(Double teaPrice) {
		this.teaPrice = teaPrice;
	}
	public String getTeaFlavor() {
		return teaFlavor;
	}
	public void setTeaFlavor(String teaFlavor) {
		this.teaFlavor = teaFlavor;
	}
}
